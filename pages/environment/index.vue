        особливо заплавної частини заповідника. Натомість на терасову частину заповідника 
        мікрокліматичні особливості заплави поширюються слабо.
      </p>
      <p>
        &emsp;&emsp;&emsp;Тривалість вегетаційного періоду 214 днів, а період активної вегетації 
        в межах 150 днів. Дата закінчення пізніх весняних заморозків – 24 квітня, а початку 
        ранніх осінніх – 12 жовтня. Особливо небажаними є пізні весняні заморозки та ще і в 
        роки раннього приходу весни. Такі заморозки побивають молоді пагони рослин і приносять 
