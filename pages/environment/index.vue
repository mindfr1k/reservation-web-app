        повітря. У континентальній кліматичній області щороку бувають посухи, суховії, пилові 
        бурі, що завдають значної шкоди сільському господарству.
      </p>
      <p>
        &emsp;&emsp;&emsp;Загалом клімат в межах заповідника характеризується як помірно 
