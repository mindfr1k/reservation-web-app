        &emsp;&emsp;&emsp;Найнижча середньомісячна температура повітря в січні (мінус 14,5°С) 
        зафіксована в 1950 р., найвища (+1,5°С) – в 2007 р. Найнижча середньомісячна температура 
        в липні (+18,4°С) спостерігалась у 1976 р., найвища (+25,6°С) – в 1936 р. Абсолютний 
        мінімум температури повітря (-38,2 °С) зафіксовано 11 січня 1940 р., абсолютний максимум 
        (+40,1°С) – 10 серпня 1930 р.
      </p>
      <p>
        &emsp;&emsp;&emsp;За останні 100-120 років температура повітря в межах заповідника, 
