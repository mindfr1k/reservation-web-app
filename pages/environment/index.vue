        сильних посух. Значна тривалість посух негативно впливає на стан рослинності та її врожайність. 
        В комплексі з суховіями, які нерідко спостерігаються в такі періоди, ці два фактори 
        спричинюють загибель рослинності, яка більше транспірує вологи через листя, чим в змозі 
        подати коренева система. Особливо це стосується молодих посівів і посадок. Відсутність 
        снігового покриву взимку, навіть не при не дуже великих морозах приводить до вимерзання 
        посівів і посадок. Часті зміни температури повітря з плюсових на мінусові і навпаки, в 
        зимовий час спричинюють вимокання лісових культур.
      </p>
