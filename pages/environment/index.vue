      <h5 class="center-align">
        <b><u>Клімат</u></b>
      </h5>
      <p>
        &emsp;&emsp;&emsp;Територія заповідника «Дніпровсько-Орільський» відноситься до 
        континентальної кліматичної області України. Ця область охоплює степову фізико-географічну 
